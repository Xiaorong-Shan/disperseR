#' create a set of directories to run disperseR
#'
#' \code{calculate_daily_exposure}
#'
#' @description `calculate_daily_exposure()` takes linked HYSPLIT outputs combined using `disperseR::combine_monthly_links()` and sums them by emissions. The results can be aggregated to three different source levels and two different time scales. Various combinations of `year.E` and `year.D` can be applied to develop counterfactual scenarios.
#'
#'
#' @param year.E emissions year
#'
#' @param year.D HYSPLIT year
#'
#' @param link.to spatial scale for plotting. One of 'zips', 'counties', or 'grids' that should match original input to disperseR::link_all_units()
#'
#' @param pollutant name of column in `units.mo` for weighting
#'
#' @param rda_file specification of data location produced by `disperseR::combine_monthly_links()`. Can denote 'loaded' if the object is loaded in the .GlobalEnv() or a file location
#'
#' @param exo_dir file path to save resulting exposure dataset
#'
#' @param source.agg source aggregation level
#'
#' @param return.daily.data logical. If TRUE, returns daily datasets. Set to FALSE to save memory space.
#'
#' @return Creates directories (does not overwrite if existing). Outputs string variables with paths to the environment.


#' @export calculate_daily_exposure

calculate_daily_exposure <- function(year.E,
                               year.D,
                               link.to = 'zips',
                               pollutant = 'SO2..tons.',
                               units.mo,
                               rda_file = 'loaded',
                               exp_dir = NULL,
                               source.agg = c('total', 'facility', 'unit'),
                               return.daily.data = F) {
  `%ni%` <- Negate(`%in%`)

  #define defaults if none provided
  if (length(source.agg) > 1) {
    message('Multiple source.agg provided, deaulting to "total".')
    source.agg <- 'total'
  }
  if (source.agg %ni% c('total', 'facility', 'unit')) {
    stop('source.agg not recognized, please provide one of c("total", "facility", "unit").')
  }

  #define defaults if none provided
  if (rda_file != 'loaded')
    load(rda_file, envir = environment())

  # start with the first map available in the given year
  # map.names <- paste0("MAP", 1:12, ".", year.D)
  # for( m in 1:12){
  #   test <- map.names[m] %in% ls(envir = globalenv()) | map.names[m] %in% ls()
  #   if( test)
  #     break
  # }

  # Create directory to store output files if it does not exist
  if (is.null(exp_dir)) {
    exp_dir <- file.path(getwd(), 'rdata_hyspdisp')
    message(paste('No exp_dir provided. Defaulting to', exp_dir))
  }
  dir.create(exp_dir, recursive = TRUE, showWarnings = F)

  #initiate exposure data.table
  exposures <-  data.table()

  #initiate list of monthly files
  daily.filelist <- c()

  #Iterate over months of the year
  message(
    paste0(
      "Calculating ", link.to, " exposures for HYSPLIT year ",
      year.D,
      " and emissions year ",
      year.E,
      "!"
    )
  )
  for (i in 1:12) {
    full.date <- paste0(year.E, "-", i, "-", 01 ) #try to get the first day of month i and year.E
    full.date <- as.Date(full.date)
    start_date <- floor_date(full.date, 'month') #the start day of month i and year.E
    end_date <- ceiling_date(full.date, 'month') %m-% days(1) #the end day of month i and year E
    length_dates <- as.numeric(as.Date(end_date) - as.Date(start_date))+1 #get the number of days in that month
    
    for(j in 1:length_dates){
      PP.units_daily <- subset(units.mo, month == i & year == year.E & day==j)
      setnames(PP.units_daily, pollutant, 'pollutant')
      
      #Aggregate unit power plant emissions to unit level
      PP_daily <- PP.units_daily[!duplicated(uID)]
      PP_daily <- PP_daily[is.na(pollutant), pollutant := 0]
      
      #get HYPSPLIT mappings
      map.name <- paste0("MAP", i, ".",j,".", year.D)
      if (map.name %ni% ls(envir = globalenv())
          & map.name %ni% ls()) {
        message(
          paste('  ',
                map.name,
                'not loaded in environment. If you want it linked, either load RData file before or specify rda_file'
          )
        )
        next
      }
      if (map.name %in% ls()) {
        day_mapping <-
          data.table(eval(parse(text = map.name)))
      } else{
        day_mapping <- data.table(eval(parse(text = map.name),
                                         envir = globalenv()))
      }
      
      #melt them to long format
      if( link.to == 'zips'){
        id.v <- 'ZIP'
        day_mapping <- day_mapping[ZIP != 'ZIP']
      } else if( link.to == 'counties'){
        id.v <- c("statefp", "countyfp", "state_name", "name", "geoid")
      } else if( link.to == 'grids')
        id.v <- c('x', 'y')
      
      day_mapping[is.na(day_mapping)] <- 0
      names(day_mapping)[names(day_mapping) %ni% 'state_name'] <-
        gsub('_|-|\\*', '.', names(day_mapping)[names(day_mapping) %ni% 'state_name'])
      
      day_mapping_long <- melt(
        day_mapping,
        id.vars = id.v,
        variable.factor = FALSE,
        variable.name = "uID",
        value.name = "N"
      )
      if (sapply(day_mapping_long, class)['N'] == 'character')
        day_mapping_long[, `:=`(N = as.double(N))]

      #This is what I want - pollutant-weighted emissions trajectories
      PP.linkage <-
        merge(day_mapping_long,
              PP_daily,
              by = 'uID',
              all.y = T)
      
      #  clean house
      rm(list = c('day_mapping_long', 'PP_daily', 'day_mapping'))
      
      # define aggregation strings
      if (source.agg == 'total'){
        sum.by <- c(id.v, 'yearmonth')
        file.by <- '_exposures_total_'
      }
      if (source.agg == 'facility'){
        sum.by <- c(id.v, 'FacID', 'yearmonth')
        file.by <- '_exposures_byfacility_'
      }
      if (source.agg == 'unit'){
        sum.by <- c(id.v, 'uID', 'yearmonth')
        file.by <- '_exposures_byunit_'
      }
      
      # add day
      PP.linkage[, `:=` (
        Exposure  = pollutant * N,
        yearmonth = paste0(year.E, formatC(i, width = 2, flag = '0'),
                            formatC(j, width = 2, flag = '0'))
      )] # add "0" before month and days to avoid ambiguity.
      print(PP.linkage)
      # Append running data frame
      exposures <- data.table(rbind(exposures,
                                    PP.linkage[, list(hyads = sum(Exposure)),
                                               by = sum.by]))[hyads > 0]
      
      # write to file, add daily file to list if not empty data.table
      file.mo <- file.path(exp_dir,
                           paste0(
                             link.to,
                             file.by,
                             paste0(year.E, '_', formatC(
                               i, width = 2, flag = '0'
                             ),'_',formatC(
                               j, width = 2, flag = '0'
                             )),
                             '.fst'
                           ))
      
      if( link.to == 'zips')
        exposures <- exposures[ZIP != '   NA']
      
      if (nrow(exposures) != 0) {
        write.fst(exposures,
                  path = file.mo)
        daily.filelist<-append(daily.filelist,file.mo)# we have multiple files per month, thus we append files into filelist.
      }
      #re-initiate ZIP exposure data.table
      exposures <-  data.frame()
    }

  }
  print(daily.filelist)
  if (return.daily.data) {
    out <- rbindlist(lapply(na.omit(daily.filelist),
                            read.fst))
    
    if( link.to == 'zips')
      out <- out[ZIP != '   NA']
    
    return(out)
  } else
    return(daily.filelist)
}
